int main()
{
    string s;
    int cu = 0, cl = 0;
    cin >> s;
    //count uppercase and lowercase
    for (auto &&i : s)
    {
        if (isupper(i))
        {
            cu++;
        }
        else
            cl++;
    }
    if (cu > cl)
    {
        char c;
        for (auto &&i : s)
        {
            c = toupper(i);
            cout << c;
        }
    }
    else
    {
        char c;
        for (auto &&i : s)
        {
            c = tolower(i);
            cout << c;
        }
    }
    return 0;
}
